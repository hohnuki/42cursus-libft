#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (1)
	{
		if (lst == NULL)
			break ;
		f(lst->content);
		lst = lst->next;
	}
}
