char *strnstr(const char *s1, const char *s2, int size)
{
	int i;

	i = 0;
	if (s2[0] == '\0' || size <= 0)
		return (s1);
	while (s1[i] == '\0' && size >= 0)
	{
		i++;
		size--;
	}
	return (0);
}
