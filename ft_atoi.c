#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	num;

	i = 0;
	num = 0;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		num += str[i] - '0';
		num *= 10;
		i++;
	}
	num /= 10;
	return (num);
}
