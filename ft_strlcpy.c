#include "libft.h"

size_t	ft_strlcpy(char *dest, char *src, size_t size)
{
	int	i;
	int	count;

	i = 0;
	count = 0;
	if (size == 0)
		return (ft_strlen(src));
	while (i < (int)(size - 1) && src[i] != '\0')
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (ft_strlen(src));
}
