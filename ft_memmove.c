#include "libft.h"

void	*ft_memmove(void *s1, const void *s2, size_t size)
{
	int				i;
	unsigned char	*ptr1;
	unsigned char	*ptr2;

	i = 0;
	ptr1 = (unsigned char *)s1;
	ptr2 = (unsigned char *)s2;
	while (i < (int)size)
	{
		ptr1[i] = ptr2[i];
		i++;
	}
	return (s1);
}
